package entities;

public class Numbers {

	private int numberPositve;
	private int numberNegative;
	
	
	public int getNumberPositve() {
		return numberPositve;
	}
	public void setNumberPositve(int numberPositve) {
		this.numberPositve = numberPositve;
	}
	public int getNumberNegative() {
		return numberNegative;
	}
	public void setNumberNegative(int numberNegative) {
		this.numberNegative = numberNegative;
	}
	public Numbers(int numberPositve, int numberNegative) {
		this.numberPositve = numberPositve;
		this.numberNegative = numberNegative;
	}
	
	
	
	
}
